
# 2.3 B. Integer (int)
int_value1 = 25
int_value2 = 2024
print("Integer values:", int_value1, int_value2)

# 2.3 C. Float (float)
float_value1 = 5.9
float_value2 = 19.99
print("Float values:", float_value1, float_value2)

# 2.3 D. Boolean (bool)
bool_value1 = True
bool_value2 = False
print("Boolean values:", bool_value1, bool_value2)

# 2.3 F. Dictionary (dict)
student = {
    "name": "Alice",
    "age": 22,
    "major": "Computer Science"
}
print("Student dictionary:", student)

# Checking Data Types
print("Data type of student:", type(student))

# Type Conversion
converted_float = int(float_value1)
converted_string = str(int_value1)

# strings.py

# 2.3 A. String (str)
string1 = "Hello, World!"
string2 = 'Python is fun!'

# String Methods
# Lower
print("Lowercase:", string1.lower())

# Upper
print("Uppercase:", string2.upper())

# Strip
string_with_space = "   Hello   "
print("Stripped string:", string_with_space.strip())

# Replace
new_string = string1.replace("World", "Python")
print("Replaced string:", new_string)
